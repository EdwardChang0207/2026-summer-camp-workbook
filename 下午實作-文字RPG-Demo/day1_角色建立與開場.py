# ── 1) 開場白：用 print() 印出固定文字 ──
print("=" * 40)
print("🏰 歡迎來到《魔法地下城》")
print("=" * 40)

# ── 2) 跟玩家對話：用 input() 取得輸入 ──
#    input() 拿到的永遠是「字串」，數字要用 int() 轉換才能計算
name = input("勇者，請報上你的名字：")
level = int(input("你的初始等級是？（請輸入數字）："))

# ── 3) 用變數做運算（這就是「角色屬性」）──
hp = level * 20      # 生命值 = 等級 × 20
attack = level * 10  # 攻擊力 = 等級 × 10

# ── 4) 用 f-string 把變數嵌進句子，印出角色卡 ──
print()
print("───── 你的角色卡 ─────")
print(f"姓名　：{name}")
print(f"等級　：{level}")
print(f"生命值：{hp}")
print(f"攻擊力：{attack}")
print("──────────────────────")
print(f"勇者 {name} 推開石門，踏入了哥布林洞穴，冒險正式開始！")
